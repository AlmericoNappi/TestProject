package com.util;

import com.model.Cittadini;

public class CittadiniWrapper {

	private Cittadini cittadino;
	private String user;
	private String pwd;
	
	public Cittadini getCittadino() {
		return cittadino;
	}
	public void setCittadino(Cittadini c) {
		this.cittadino = c;
	}
	public String getUser() {
		return user;
	}
	public void setUser(String user) {
		this.user = user;
	}
	public String getPwd() {
		return pwd;
	}
	public void setPwd(String pwd) {
		this.pwd = pwd;
	}
	
	
}
